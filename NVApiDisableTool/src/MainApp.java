    public void checkFile(){

        File file = new File("C:/Windows/System32/nvapi64.dll");
        File file2 = new File("C:/Windows/System32/nvapi64_.dll");

        if (file2.exists()){
            status.setText("nvapi64.dll disabled");
            status.setForeground(Color.RED);
        }else if(file.exists()){
            status.setText("nvapi64.dll enabled");
            status.setForeground(Color.GREEN);
        }
    }

