    private void switchBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_switchBtnActionPerformed

        File file = new File("C:/Windows/System32/nvapi64.dll");
        File file2 = new File("C:/Windows/System32/nvapi64_.dll");
        
        boolean success = false;

        if (file2.exists()){
            success = file2.renameTo(file);
        }else if(file.exists()){
            success = file.renameTo(file2); 
        }
        
        if(success){
            logText.setText(logText.getText()+"SUCCESS!!!\n");
        }else{
            logText.setText(logText.getText()+"ERROR You need administrator rights\n");
        }
        
        if(!file.exists()&&!file2.exists()){
            logText.setText(logText.getText()+"ERROR nvapi64.dll not found\n");
        }
        
        checkFile();
    }//GEN-LAST:event_switchBtnActionPerformed

